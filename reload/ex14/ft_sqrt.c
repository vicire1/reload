int	ft_sqrt(int nb)
{
	long	i;

	i = 1;
	if (nb < 0 || nb == 0)
		return (0);
	while (i * i <= nb)
	{
		if (nb / i == i && nb % i == 0)
			return (i);
		i++;
	}
	return (0);
}
